package Chuong2.BaiTap;

public class BtPhan2 {
    public static void main(String[] args) {
        /* Sử dụng vòng lặp để in ra các số từ 1 đến 10 trên một dòng, dùng kí tự tab
('\t') để ngăn cách giữa các số. */
        for (int i = 1; i <= 10; i++) {
            System.out.print(i + "\t");
        }
        System.out.println();
        System.out.printf("Hello, I am %s, I am %d years old.\n", "Vinh", 20 );




    }
}
